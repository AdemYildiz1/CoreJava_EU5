package day8_controlFlowStatements_Part2;

public class Task30_OddAndEvenNumber {

	public static void main(String[] args) {

        //we use modules(%2) for even and odd number
		int number = 5;
		
		if (number%2==0) {
			System.out.println("Number:"+ number+" is even number");
		}else {
			System.out.println("Number:"+ number+" is odd number");
			
		}

	}

}
