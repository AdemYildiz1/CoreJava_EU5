package day21_StringManupilation_part3;

public class String_ToLowerCasesAnd_ToUpperCases {

	public static void main(String[] args) {
		// toLowerCases() and toUpperCases() methods returns everything in string lower or upper cases
		
		String word="AUTOMATION";
		System.out.println(word.toLowerCase());
		
		String word2="cool is NOT COOLING";
		System.out.println(word2.toUpperCase());
	}

}
